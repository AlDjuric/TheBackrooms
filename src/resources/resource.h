/**
 * @file resource.h
 * @brief Windows resource definitions for The Backrooms game
 * 
 * Contains resource identifiers for Windows executable resources
 * such as icons, bitmaps, and other embedded assets.
 */

#ifndef RESOURCE_H
#define RESOURCE_H

/**
 * @brief Application icon resource identifier
 * 
 * Defines the resource ID for the main application icon that will
 * be displayed in the Windows taskbar, window title bar, and file explorer.
 * The icon file is embedded into the executable during compilation.
 * 
 * @note Value 101 is a standard starting ID for Windows application icons
 * @note This ID must match the one used in resource.rc
 */
#define IDI_ICON1 101

#endif /* RESOURCE_H */